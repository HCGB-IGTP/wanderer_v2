cd $WDIR


# for name in ${DAT[@]}
for name in ${DAT[@]}
do
    for ctype in ${CTYPE[@]}
    do
        for platform in ${PLATFORM[@]}
        do
            # catching the data source inconsistent filenaming
            if [ "$platform" = "RNAseqGene" ]
            then
                PLATFORMFN=RNAseq_Gene
            else
                PLATFORMFN=RNAseq
